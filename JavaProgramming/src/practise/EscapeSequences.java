package practise;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hi,How are u \nGood"); // \n new line next line printing
        System.out.println("\tJava is fun"); // \ horizontal tab nex tab
        System.out.println("\\"); // backslash only to be printed \\
        System.out.println("My fav prog lang is \"Java\""); // \" double quotation mark printed "Java"

    }

}

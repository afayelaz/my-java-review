package practise;

public class RelationalOps {

    public static void main(String[] args) {
        // > , >= , < , <= , ==, !=

        boolean res1 = 10>11;
        System.out.println(res1);

        boolean res2 = 10==11;
        System.out.println(res2);

        boolean res3 = 10!=11;
        System.out.println(res3);

    }
}
